// Char data type
fun main(args: Array<String>) {
    // Character Variable
    var charA : Char = 'A'
    var charZ : Char = 'Z'
    var char1 = '1'
    var char0 = '0'
    println("$charA $charZ $char1 $char0")
    var char5 = '#'
    var char6 = '/'
    var char7 = '%'
    println("$char5 $char6 $char7")
    // Unicode Character
    var ucharA : Char = '\u0041'
    var ucharZ : Char = '\u005A'
    var uchar1 = '\u0031'
    var uchar0 = '\u0030'
    println("$ucharA $ucharZ $uchar1 $uchar0")
}
