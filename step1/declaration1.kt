//declaration and initialization
fun main(args: Array<String>) {
    // Variables can be initialized explicitly
    var myName: String
    // Initialization
    myName = "Ama"
    // declaration and initialization in one line
    var myInt: Int = 10
    var myLong: Long = 11
    var myShort: Short = 11
    var myByte: Byte = -128
    // Declaration of Double and Float
    var h1 = 5.10
    var h2: Double = 5.10
    var f1 = 5.10
    var f2: Float = 5.10f
}


